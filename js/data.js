/* 別ルートの1本目として有効なダーツ一覧を返す */
function _getAltFirstDarts(score, path) {
  if (!path || path.length < 2 || !score) return [];
  var canonical = path[0];
  var nDarts = path.length;
  var alts = [];
  var _seen = {};
  var allCandidates = _ARR_TRIPLE_POOL.concat(_ARR_DOUBLE_POOL).concat(_ARR_SINGLE_POOL).concat(['Bull','25']).filter(function(d){ return _seen[d] ? false : (_seen[d]=true); });
  function dartVal(label) {
    if (label === 'Bull') return 50;
    if (label === '25') return 25;
    if (label[0] === 'T') return parseInt(label.slice(1)) * 3;
    if (label[0] === 'D') return parseInt(label.slice(1)) * 2;
    return parseInt(label);
  }
  function isValidDouble(n) {
    return n === 50 || (n >= 2 && n <= 40 && n % 2 === 0);
  }
  for (var i = 0; i < allCandidates.length; i++) {
    var label = allCandidates[i];
    if (label === canonical) continue;
    var rem = score - dartVal(label);
    if (rem < 2) continue;
    var valid = false;
    if (nDarts === 2) {
      valid = isValidDouble(rem);
    } else if (nDarts >= 3) {
      valid = rem <= 170 && !!CHECKOUT[rem] && CHECKOUT[rem].length <= 2;
    }
    if (valid) alts.push(label);
  }
  return alts;
}

