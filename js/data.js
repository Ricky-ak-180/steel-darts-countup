/* 重み付きサンプリング（非復元） */
function _arrWeightedSample(pool, weightFn, n) {
  var items = pool.slice();
  var result = [];
  while (result.length < n && items.length > 0) {
    var weights = items.map(weightFn);
    var total = weights.reduce(function(s, w) { return s + w; }, 0);
    var r = Math.random() * total, cum = 0, picked = 0;
    for (var i = 0; i < weights.length; i++) { cum += weights[i]; if (r < cum) { picked = i; break; } }
    result.push(items[picked]);
    items.splice(picked, 1);
  }
  return result;
}

