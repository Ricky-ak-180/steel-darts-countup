// スコアから最短チェックアウトパスを返す（ユーザールート優先、≤40の偶数にも対応）
function _getPath(n) {
  // ユーザーが登録したルートがあれば最優先
  if (_userRoutes && _userRoutes[n] && _userRoutes[n].length > 0) return _userRoutes[n][0];
  if (n === 50) return ["Bull"];
  if (n === 25 && _z01.outRule !== 0) return ["S-BULL"];
  if (CHECKOUT[n]) return CHECKOUT[n];
  if (n > 0 && n <= 40 && n % 2 === 0) return ["D" + (n / 2)];
  return null;
}

