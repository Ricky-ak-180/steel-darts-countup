var _BOARD_SEGS = [20,1,18,4,13,6,10,15,2,17,3,19,7,16,8,11,14,9,12,5];

function _parseDartNotation(d) {
  if (!d) return null;
  if (d === 'Bull') return {n:50, ring:'Bull'};
  if (d === '25' || d === 'SBull' || d === 'S-BULL') return {n:25, ring:'SBull'};
  if (d[0]==='T') return {n:parseInt(d.slice(1)), ring:'T'};
  if (d[0]==='D') return {n:parseInt(d.slice(1)), ring:'D'};
  if (d[0]==='S') return {n:parseInt(d.slice(1)), ring:'S'};
  return {n:parseInt(d), ring:'S'};
}

