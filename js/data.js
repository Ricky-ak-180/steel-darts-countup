function _parseRouteInput(str) {
  // "T20 D20" or "T20,D20" or "T20, D20" → ["T20","D20"]
  var parts = str.trim().toUpperCase().split(/[\s,]+/).filter(Boolean);
  // 簡易バリデーション: T/D/S+数字 or Bull
  var valid = parts.every(function(p) {
    return /^(T|D|S)\d{1,2}$/.test(p) || p === 'BULL';
  });
  return valid && parts.length >= 1 ? parts : null;
}

