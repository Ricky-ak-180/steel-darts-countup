function _renderArrWeak(qd, mode) {
  var isKezuri = (mode === 4);
  var entries = [];
  if (isKezuri) {
    for (var i = 0; i < _ARR_KEZURI_QS.length; i++) {
      var key = 'kz_' + i;
      if (qd[key] && qd[key].a > 0) {
        entries.push({ label: _ARR_KEZURI_QS[i].q, sub: '', c: qd[key].c, a: qd[key].a, acc: qd[key].c / qd[key].a });
      }
    }
  } else {
    var modeScores = _ARR_SCORES[mode];
    for (var i = 0; i < modeScores.length; i++) {
      var key = String(modeScores[i]);
      if (qd[key] && qd[key].a > 0) {
        var path = (CHECKOUT[modeScores[i]] || []).join(' → ');
        entries.push({ label: String(modeScores[i]), sub: path, c: qd[key].c, a: qd[key].a, acc: qd[key].c / qd[key].a });
      }
    }
  }
  if (entries.length === 0) return '';
  entries.sort(function(a, b) { return a.acc - b.acc || b.a - a.a; });
  var worst = entries.slice(0, 5);
  var title = isKezuri ? '📌 苦手な削り問題' : '📌 苦手チェックアウト';
  var html = '<div class="arr-weak-wrap">';
  html += '<div class="arr-weak-title">' + title + '</div>';
  html += '<div class="arr-weak-list">';
  for (var j = 0; j < worst.length; j++) {
    var e = worst[j];
    var pct = Math.round(e.acc * 100);
    var barColor = pct <= 30 ? '#ef5350' : pct <= 60 ? '#ffa726' : '#66bb6a';
    html += '<div class="arr-weak-row">';
    if (isKezuri) {
      html += '<span class="arr-weak-path" style="flex:1;white-space:normal;font-size:11px;color:var(--fg)">' + e.label + '</span>';
    } else {
      html += '<span class="arr-weak-score">' + e.label + '</span>';
      html += '<span class="arr-weak-path">' + e.sub + '</span>';
    }
    html += '<span class="arr-weak-pct" style="color:' + barColor + '">' + pct + '%</span>';
    html += '</div>';
  }
  html += '</div></div>';
  return html;
}

