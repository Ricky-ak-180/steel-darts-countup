// dart(例:"T18")を cur点から投げたときの全可能アウトカム（CHECKOUTが続くものだけ）
function _getMissOptions(dart, cur) {
  var opts = [];
  function tryAdd(type, val, missTarget) {
    var ns = cur - val;
    if (ns < 0) return;
    if (ns === 0 || _getPath(ns)) {
      var lbl = type === 'success'
        ? val + '　入った！'
        : val + '　に外れた！';
      opts.push({ type: type, val: val, newScore: ns, dart: dart, missTarget: missTarget, label: lbl });
    }
  }
  var sv = _dartVal(dart);
  tryAdd('success', sv, null);
  if (dart === 'Bull') {
    tryAdd('miss', 25, 'S-BULL');
  } else {
    var num = parseInt(dart[0]==='T'||dart[0]==='D' ? dart.slice(1) : dart);
    var ring = dart[0]==='T' ? 'T' : dart[0]==='D' ? 'D' : 'S';
    // S[同じ番号]ミス
    tryAdd('miss', num, 'S'+num);
    // 隣接番号ミス
    (_ADJACENT[num]||[]).forEach(function(a){
      tryAdd('miss', a, 'S'+a);               // シングル隣接
      if (ring==='T') tryAdd('miss', a*3, 'T'+a); // トリプル隣接（トリプル狙いのみ）
    });
  }
  // newScoreの重複除去（最初に追加されたものを残す）
  var seen = {};
  return opts.filter(function(o){
    if (seen[o.newScore]) return false;
    seen[o.newScore] = true; return true;
  });
}

