function _arrShuffle(a) {
  for (var i = a.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1));
    var t = a[i]; a[i] = a[j]; a[j] = t;
  }
  return a;
}

// ダーツ記号の得点
function _dartVal(s) {
  if (s === "Bull") return 50;
  if (s[0] === "T") return parseInt(s.slice(1)) * 3;
  if (s[0] === "D") return parseInt(s.slice(1)) * 2;
  return parseInt(s) || 0;
}

// 外れてシングルに当たった場合の得点
function _missVal(s) {
  if (s === "Bull") return 25;       // S-BULL
  if (s[0] === "T") return parseInt(s.slice(1));
  if (s[0] === "D") return parseInt(s.slice(1));
  return 0;                          // シングル完全ミス
}

