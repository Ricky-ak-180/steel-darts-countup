function _parseRouteInput(str) {
  // "T20 D20" or "T20,D20" or "T20, D20" → ["T20","D20"]
  var parts = str.trim().toUpperCase().split(/[\s,]+/).filter(Boolean);
  // 簡易バリデーション: T/D/S+数字 or Bull
  var valid = parts.every(function(p) {
    return /^(T|D|S)\d{1,2}$/.test(p) || p === 'BULL';
  });
  return valid && parts.length >= 1 ? parts : null;
}

function _addUserRoute(score) {
  var row = document.querySelector('.arr-t-row[data-score="' + score + '"]');
  if (!row) return;
  // 入力欄を展開
  var existing = row.querySelector('.arr-t-input-row');
  if (existing) { existing.querySelector('.arr-t-input').focus(); return; }
  var inputRow = document.createElement('div');
  inputRow.className = 'arr-t-input-row';
  inputRow.innerHTML =
    '<input class="arr-t-input" placeholder="例: T16 D14" maxlength="30">' +
    '<button class="arr-t-input-ok">追加</button>';
  row.querySelector('.arr-t-routes').appendChild(inputRow);
  var inp = inputRow.querySelector('.arr-t-input');
  inp.focus();
  inputRow.querySelector('.arr-t-input-ok').addEventListener('click', function() {
    var path = _parseRouteInput(inp.value);
    if (!path) { inp.style.borderColor='#ef5350'; return; }
    if (!_userRoutes[score]) _userRoutes[score] = [];
    _userRoutes[score].push(path);
    _saveUserRoutes();
    _renderTeiseki();
    // 追加後スクロール位置を維持
    var newRow = document.querySelector('.arr-t-row[data-score="' + score + '"]');
    if (newRow) newRow.scrollIntoView({block:'nearest'});
  });
  inp.addEventListener('keydown', function(e) {
    if (e.key === 'Enter') inputRow.querySelector('.arr-t-input-ok').click();
    if (e.key === 'Escape') inputRow.remove();
  });
}

